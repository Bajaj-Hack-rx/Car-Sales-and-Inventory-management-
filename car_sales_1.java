    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        model=(DefaultTableModel)jTable1.getModel();
        num = model.getValueAt(jTable1.getSelectedRow(),0).toString();
        String name = model.getValueAt(jTable1.getSelectedRow(),1).toString();
        String cond = model.getValueAt(jTable1.getSelectedRow(),2).toString();
        String dist = model.getValueAt(jTable1.getSelectedRow(),3).toString();
        String pric = model.getValueAt(jTable1.getSelectedRow(),4).toString();
        model_no.setText(num);
        model_name.setText(name);
        condition.setText(cond);
        distance.setText(dist + " km");
        int p = Integer.parseInt(pric);
        if("New".equals(cond))
              p=(int) (p+p*0.4);
        else if("1 - 5 yrs".equals(cond))
              p=(int) (p+p*0.2);
        else if("6 - 10 yrs".equals(cond))
              p=(int) (p-p*0.2);  
        else
              p=(int) (p-p*0.4);  
        pric=Integer.toString(p);
        price.setText(pric);
        
    }//GEN-LAST:event_jTable1MouseClicked

