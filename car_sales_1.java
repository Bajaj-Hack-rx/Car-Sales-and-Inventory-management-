     public void refresh()
    {
        try
        {
                    model=(DefaultTableModel)jTable1.getModel();
                    model.setRowCount(0);
                    Class.forName("com.mysql.jdbc.Driver");
                    String query="SELECT  `model`, `name`, `condition`, `distance`, `baseprice` FROM `carsales`";
                    con=DriverManager.getConnection("jdbc:mysql://localhost/carsales","root","");
                    Statement st = con.createStatement();
                    ResultSet rs=st.executeQuery(query);
                    while(rs.next())
                    {
                        String id=rs.getString(1);
                        String prodname = rs.getString(2);
                        String cond=rs.getString(3);
                        String dist = rs.getString(4);
                        String base = rs.getString(5);
                        String tbData[ ]={id,prodname,cond,dist,base};
                        
                       model.addRow(tbData);
                    }
        } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
        }
    }

